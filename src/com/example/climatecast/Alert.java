package com.example.climatecast;

public class Alert {

	String locationName, rainState, targetLow, targetHigh;

	public String getLocationName() {
		return locationName;
	}

	public void setLocationName(String locationName) {
		this.locationName = locationName;
	}

	public String getRainState() {
		return rainState;
	}

	public void setRainState(String rainState) {
		this.rainState = rainState;
	}

	public String getTargetLow() {
		return targetLow;
	}

	public void setTargetLow(String targetLow) {
		this.targetLow = targetLow;
	}

	public String getTargetHigh() {
		return targetHigh;
	}

	public void setTargetHigh(String targetHigh) {
		this.targetHigh = targetHigh;
	}

}
